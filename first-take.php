                            (9/22/21) - It's a hot topic in banking right now: the IRS reporting plan proposed as part of the US Department of Treasury budget reconciliation package. 
                            Under the measure, FI’s would be required to annually report gross inflows and outflows from all business and personal accounts including bank, loan, and investment accounts. If the inflows and out flows of an account total at least $600 in a year or if the fair market value is at least $600. 
                            (Politifact article by Tom Kertscher)
                        </p><br>
                        <p>    
                            The proposal, according to the US Department of Treasury, would be focused on higher earners who do not fully report their tax liabilities. No details of individual transactions would be reported. 
                            (Politifact article by Tom Kertscher)<br>
                        </p>
                        <p>
                            If passed, this proposal would essentially add two numbers to a form already submitted (1099). 
                            (American Banker article by Brendan Pedersen)<br>
                        </p>
                        <p>
                            The plan is only currently a recommendation from the Biden Administration as part of the American Families Plan and Build 
                            Back Better Agenda. The proposal, which is part of larger budget package, still would need to make it out of committee, through Congress, and to the President's desk before becoming law.
                             Oftentimes, these packages come out of committee unrecognizable if they even make it out at all.
                        </p><br>
                        <p>
                            There are many organizations, politicians, and individuals that have come out against the proposed reporting plan. This is includes a letter signed by several members of Congress(Opens in a new Window) stating their objection to this item. In the note sent to Speaker of the House Nancy Pelosi, Chairman Richard Neal of the Ways and Means Committee,
                             Commissioner Charles Rettig of the IRS, and Treasury Secretary Janet Yellen, lawmakers who signed the letter stated, "We are concerned about a recent IRS data collection proposal to increase tax information reporting requirements on financial institutions, which we do not believe are necessary or helpful toward closing the 'Tax Gap'." 
                            You can read the full letter and see those who signed by clicking here(Opens in a new Window).
                        </p><br>
                        <p>
                            Also coming out against the proposal was the American Banker's Association. "The proposal would create a significant burden on small businesses and community banks and add no discernible value to tax enforcement. It is far from clear that requiring banks to report on every single customer financial account with grows inflows and outflows above $600 – creating a mountain of new data – will lead to better tax compliance," 
                            the association said to Politifact reporter Tom Kertscher. (Politifact article by Tom Kertscher)
                        </p><br>
                        <p>
                            Once again, this legislation still has a long way to go before ever becoming law. That being said, Swiss Apex Private Bank 
